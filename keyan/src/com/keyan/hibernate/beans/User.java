package com.keyan.hibernate.beans;

/**
 * User entity. @author MyEclipse Persistence Tools
 */

public class User implements java.io.Serializable {

	// Fields
	private int id;
	//private UserId id;
	
	private String name;
	private String userName;
	private String password;
	private String department;
	private String positionalTitles;
	private int workloadLevel;
	private String groups_id;
	// Constructors


	public User() {
	}

	/** minimal constructor */
	public User(int id, String name, String userName,String password,
			 String department,String positionalTitles,int workloadLevel) {
		this.id = id;
		this.name = name;
		this.userName = userName;
		this.password = password;
		this.department = department;
		this.positionalTitles = positionalTitles;
		this.workloadLevel = workloadLevel;
	}

	/** full constructor */
		// Property accessors

	public int getId() {
		return this.id;
	}

	public void setId(int id) {
		this.id = id;
	}

	
	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUserName() {
		return this.userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return this.password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getDepartment() {
		return this.department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	

	public String getPositionalTitles() {
		return positionalTitles;
	}

	public void setPositionalTitles(String positionalTitles) {
		this.positionalTitles = positionalTitles;
	}

	public String getGroups_id() {
		return groups_id;
	}

	public void setGroups_id(String groups_id) {
		this.groups_id = groups_id;
	}

	public int getWorkloadLevel() {
		return workloadLevel;
	}

	public void setWorkloadLevel(int workloadLevel) {
		this.workloadLevel = workloadLevel;
	}

}